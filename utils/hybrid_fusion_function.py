import numpy as np
from typing import Union, List
from langchain_classic.schema import Document
from langchain_core.tools import tool

def hybrid_scores(chroma_score_: Union[float, List[float]],
                    bm25_score_: Union[float, List[float]],
                    bm25_scaling_weight=0.05,
                    alpha=0.5): # alpha：chroma占比

    # 数值转换：将chroma余弦距离转化为余弦相似度（用1减）
    if isinstance(chroma_score_, (int, float)):
        chroma_score_ = 1 - chroma_score_
    else:
        chroma_score_ = [1 - x for x in chroma_score_]


    # 用带缩放的sigmoid函数将bm25的值压缩到0-1之间
    def sigmoid_with_scaling(x, scaling_factor):
        return 1 / (1 + np.exp(-scaling_factor * x))
    
    # 将输入转换为 numpy 数组以统一处理
    chroma_array = np.array(chroma_score_)
    bm25_array = np.array(bm25_score_)
    
    bm25_score_scaled = sigmoid_with_scaling(bm25_array, bm25_scaling_weight)
    hybrid_score = alpha * chroma_array + (1 - alpha) * bm25_score_scaled
    
    # 如果输入是单个数字，返回标量；如果输入是列表，返回列表
    if isinstance(chroma_score_, (int, float)) and isinstance(bm25_score_, (int, float)):
        return float(hybrid_score.item())
    else:
        return hybrid_score.tolist()


@tool
def hybrid_function(chroma_docs_with_scores,
                    bm25_docs_with_scores,
                    bm25_scaling_weight=0.05,
                    alpha=0.5, # alpha：chroma占比
                    top_k=5,
                    print_details=False,
                    return_score=True):
    """
    混合两种检索方式的结果，返回综合得分最高的top_k个文档

    Args:
        chroma_docs_with_scores: Chroma检索结果，每个元素为(文档, 得分)元组
        bm25_docs_with_scores: BM25检索结果，每个元素为(文档, 得分)元组
        bm25_scaling_weight: BM25得分的缩放权重，用于sigmoid函数
        alpha: 混合权重，alpha * chroma_score + (1-alpha) * bm25_score_scaled
        top_k: 返回的文档数量
        print_details: 是否打印详细信息
        return_score: 是否返回详细分数信息
            - True: 返回 [{'doc': doc, 'hybrid_score': score, 'chroma_score': chroma_score, 'bm25_score': bm25_score}, ...]
            - False: 返回 [doc1, doc2, ...]

    Returns:
        如果return_score=True：返回包含文档和各项分数的字典列表
        如果return_score=False：返回文档列表
    """
    if print_details:
        print(f"\n=== 混合检索结果 ===")

    # 创建文档到得分的映射
    doc_scores = {}

    # 处理Chroma结果
    for doc, score in chroma_docs_with_scores:
        # 使用文档内容作为键（或者可以使用文档的其他唯一标识）
        doc_key = doc.page_content[:100]  # 使用前100字符作为标识
        if doc_key not in doc_scores:
            doc_scores[doc_key] = {'doc': doc, 'chroma_score': score, 'bm25_score': None}
        else:
            doc_scores[doc_key]['chroma_score'] = score

    # 处理BM25结果
    # 处理BM25结果（hybrid_fusion_function.py 第65行左右）
    for doc, score in bm25_docs_with_scores:
        doc_key = doc.page_content[:100]
        if doc_key not in doc_scores:
            # ✅ 仅在BM25中出现的文档，Chroma距离设为1.0 → 转换后相似度=1-1.0=0
            doc_scores[doc_key] = {'doc': doc, 'chroma_score': 1.0, 'bm25_score': score}
        else:
            doc_scores[doc_key]['bm25_score'] = score

    # 计算混合得分
    temp_results = []
    for doc_key, scores in doc_scores.items():
        chroma_score = scores['chroma_score']
        bm25_score = scores['bm25_score']

        # 如果文档只在Chroma中出现，BM25得分设置为0（因为BM25得分本身会通过sigmoid缩放）
        if bm25_score is None:
            bm25_score = 0.0

        # 计算混合得分
        hybrid_score = hybrid_scores(chroma_score, bm25_score,
                                     bm25_scaling_weight, alpha)

        # 临时存储所有信息用于打印
        temp_results.append({
            'doc': scores['doc'],
            'hybrid_score': hybrid_score,
            'chroma_score': chroma_score,
            'bm25_score': bm25_score
        })

    # 按混合得分降序排序
    temp_results.sort(key=lambda x: x['hybrid_score'], reverse=True)

    # 取前top_k个
    top_results = temp_results[:top_k]

    # 根据return_score参数决定返回格式
    if return_score:
        # 返回包含文档和各项分数的字典列表
        formatted_results = [
            {
                'doc': item['doc'],
                'hybrid_score': item['hybrid_score'],
                'chroma_score': item['chroma_score'],
                'bm25_score': item['bm25_score']
            }
            for item in top_results
        ]
    else:
        # 只返回文档列表
        formatted_results = [item['doc'] for item in top_results]

    if print_details:
        for i, item in enumerate(top_results):
            print(f"\n--- 第 {i + 1} 名 (混合得分: {item['hybrid_score']:.4f}) ---")
            print(f"Chroma得分: {item['chroma_score']:.4f}")  # 注意：这里将余弦相似度转回余弦距离
            print(f"BM25得分: {item['bm25_score']:.4f}")
            print(f"文档来源: {item['doc'].metadata.get('source', '未知')}")
            print(f"文档内容预览: {item['doc'].page_content}")

    return formatted_results

